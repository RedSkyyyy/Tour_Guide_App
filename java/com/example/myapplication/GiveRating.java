public class GiveRating extends AppCompatActivity {

    Button sendButton;
    EditText uploadComment;
    RatingBar uploadRate;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.give_rating);

        uploadRate = findViewById(R.id.rate);
        uploadComment = findViewById(R.id.comment);
        sendButton = findViewById(R.id.sendButton);

        sendButton.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {
                saveData();
            }
        });
    }

    public void saveData(){
        uploadData();
    }

    public void uploadData(){

        Float rate = uploadRate.getRating();
        String desc = uploadComment.getText().toString();

        RatingData ratingData = new RatingData(rate,desc);

        //We are changing the child from title to currentDate,
        // because we will be updating title as well and it may affect child value.

        String currentDate = DateFormat.getDateTimeInstance().format(Calendar.getInstance().getTime());

        FirebaseDatabase.getInstance().getReference("Rating").child(currentDate)
                .setValue(ratingData).addOnCompleteListener(new OnCompleteListener<Void>() {
                    @Override
                    public void onComplete(@NonNull Task<Void> task) {
                        if (task.isSuccessful()){
                            Toast.makeText(GiveRating.this, "Saved", Toast.LENGTH_SHORT).show();
                            finish();
                        }
                    }
                }).addOnFailureListener(new OnFailureListener() {
                    @Override
                    public void onFailure(@NonNull Exception e) {
                        Toast.makeText(GiveRating.this, e.getMessage().toString(), Toast.LENGTH_SHORT).show();
                    }
                });
    }
}